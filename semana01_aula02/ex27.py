C = float(input("Digite uma temperatura em graus Celsius (°C):"))

F = (9/5)*C + 32

print(f"A temperatura de {C} °C (Celsius) é igual a {F} °F (Fahrenheit)")

F = float(input("Digite uma temperatura em graus Fahrenheit (°F):"))

C = (F - 32)/(9/5)

print(f"A temperatura de {F} °F (Fahrenheit) é igual a {C} °C (Celsius).")